    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (pnome.getText().equals("") || psobrenome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos vazios! \nVerifique por favor!");
        } else if (qde == 10) {
            JOptionPane.showMessageDialog(this, "Estouro de array!");
        } else {
            Pessoa novapes = new Pessoa();
            novapes.setNome(pnome.getText());
            novapes.setSobrenome(psobrenome.getText());
            pes[qde] = novapes;
            qde++;
            aux = qde;
            pnome.setText("");
            psobrenome.setText("");
            position.setText(Integer.toString(aux));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (aux > 0) {
            Pessoa pessoaback = new Pessoa();
            aux--;
            pessoaback = pes[aux];
            pnome.setText(pessoaback.getNome());
            psobrenome.setText(pessoaback.getSobrenome());
            position.setText(Integer.toString(aux));
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (aux < qde - 1 && qde > 1) {
            Pessoa pessoanext = new Pessoa();
            aux++;
            pessoanext = pes[aux];
            pnome.setText(pessoanext.getNome());
            psobrenome.setText(pessoanext.getSobrenome());
            position.setText(Integer.toString(aux));
        }

    }//GEN-LAST:event_jButton3ActionPerformed

